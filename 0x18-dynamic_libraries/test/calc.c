#include "../main.h"

/**
 * add - adds two integers
 * @a: integer
 * @b: integer
 * Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: integer
 * @b: integer
 * Return: value of sub
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: integer
 * @b: integer
 * Return: value of mul
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div two integers
 * @a: integer
 * @b: integer
 * Return: value of div
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - returns remainder of division
 * @a: integer
 * @b: integer
 * Return: value of mod
 */
int mod(int a, int b)
{
	return (a % b);
}
